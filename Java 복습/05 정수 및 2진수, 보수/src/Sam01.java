public class Sam01 {

    public static void main(String[] args) {

        int i1;     // 선언
        i1 = 0;     // 할당

        int i2 = 0;     // 선언을 하며 할당하기

        // int -> 32bit
        // 2진수 -> 00000000_00000000_00000000_00000000

        int i3 = 10;     // 10 진수의 형태로 저장
        int i4 = 0b10;   // 2진수
        int i5 = 0x10;   // 16진수
        int i6 = 010;    // 8진수

        System.out.println(i3); // 10
        System.out.println(i4); // 2
        System.out.println(i5); // 16
        System.out.println(i6); // 8

        //    7
        //  + 3
        //   10

        // 보수를 통해서 + 를 진행


    }

}
