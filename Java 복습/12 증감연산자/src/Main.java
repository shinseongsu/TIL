public class Main {

    public static void main(String[] args) {

        int i21 = 10;
        System.out.println(i21);

        // 증가 연산자
        ++i21;
        System.out.println(i21);

        // 전위 연산자
        i21++;
        System.out.println(i21);

        System.out.println("-----------------");

        System.out.println(++i21);   // 출력할 때 +1
        System.out.println(i21++);   // 출력 후,  +1

        System.out.println("-----------------");

        System.out.println(i21);

    }

}
